package com.asac.helloworld.model;

public class SongDTO {

    String Album;
    String title;

    public SongDTO() {
    }

    public String getAlbum() {
        return Album;
    }

    public void setAlbum(String album) {
        Album = album;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

}
